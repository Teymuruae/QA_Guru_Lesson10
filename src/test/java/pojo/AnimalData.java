package pojo;

import java.util.List;

public class AnimalData {
    private String pet;
    private String name;
    private List<String> hobbies;
    private Meal meal;

    public String getPet() {
        return pet;
    }

    public String getName() {
        return name;
    }

    public List<String> getHobbies() {
        return hobbies;
    }

    public Meal getMeal() {
        return meal;
    }
}
